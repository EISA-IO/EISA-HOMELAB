    param($Env, [bool]$UseTunnel)
    Write-Banner 'Done — service map'
    if ($UseTunnel -and $Env['DOMAIN']) {
        $d = $Env['DOMAIN']
        Write-Info "Public (via Cloudflare tunnel + Authelia SSO):"
        Write-Info "  hub.$d, chat.$d, music.$d, movie.$d, file.$d, n8n.$d, search.$d, ..."
    }
    Write-Info ''
    Write-Info 'Local (LAN):'
    Write-Info '  Heimdall   http://localhost:8080'
    Write-Info '  Authelia   http://localhost:9091'
    Write-Info '  Jellyfin   http://localhost:9014'
    Write-Info '  Navidrome  http://localhost:4533'
    Write-Info '  Filebrowser http://localhost:8095'
    Write-Info '  Open WebUI http://localhost:9002'
    Write-Info '  Ollama API http://localhost:11434'
    Write-Info '  SearXNG    http://localhost:8031'
    Write-Info '  n8n        http://localhost:5678'
    Write-Info '  Portainer  http://localhost:9000'
    Write-Info '  Tor Browser (zero-click auto-login):'
    Write-Info '    http://tor.localhost/'
}

# --- run ---------------------------------------------------------------
Test-Docker
$result = Invoke-Wizard
Render-Templates -Env $result.Env
if (-not $NoStart) {
    Start-Stack -UseTunnel $result.UseTunnel
}
Show-Summary -Env $result.Env -UseTunnel $result.UseTunnel
